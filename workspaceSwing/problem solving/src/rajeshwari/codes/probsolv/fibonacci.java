package rajeshwari.codes.probsolv;

public class fibonacci {
	public static void main(String[] args){
		int n1=1;
		int n2=2;
		int n3;
		long sumOfEven = n1 + n2;
		for (int i=2;i<4000000; i++){
			n3 = n1 + n2;
			n1=n2;
			n2=n3;
			if (n3 % 2 == 0) {
				sumOfEven +=n3;
			}
		}
		System.out.println("The sum of even numbers in fibonacci series is:"+" "+sumOfEven);
	}

}
